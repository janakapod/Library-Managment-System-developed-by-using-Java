    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String un = jTextField1.getText();
        try {
            Connection con = DB.myCon();
            Statement s = con.createStatement();
            //check if field is empty
            if (!"".equals(un)) {
                /* ResultSet rs=s.executeQuery("SELECT EID FROM user where EID='un'");
                 String eid=rs.toString();*/
                //do not allow to delete main admin
                if ("E100".equals(un)) {
                    JOptionPane.showMessageDialog(this, "You cannot Delete this Account ", "Error !", JOptionPane.ERROR_MESSAGE);
                } 
                else {
                    //delete an user from the system
                    s.executeUpdate("DELETE FROM user WHERE EID='" + un + "'");
                    JOptionPane.showMessageDialog(rootPane, "Successfuly Deleted");
                }
            } 
            else {
                JOptionPane.showMessageDialog(this, "No result for EID : " + un, "Error !", JOptionPane.ERROR_MESSAGE);
            }
            con.close();
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "" + e.getMessage(), "Error !", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

