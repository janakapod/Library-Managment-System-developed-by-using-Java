    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection con = DB.myCon();
            Statement st = con.createStatement();
            ResultSet result = st.executeQuery("SELECT EID,name,userName,position FROM user");
            //assign jtable to a DefaultTableModel
            DefaultTableModel tb = (DefaultTableModel) table.getModel();
            //zero rows in table
            tb.setRowCount(0);
            //view EID, name, username and position of users in user table
            while (result.next()) {
                Vector v = new Vector();
                
                v.add(result.getString("EID"));
                v.add(result.getString("name"));
                v.add(result.getString("userName"));
                v.add(result.getString("position"));

                tb.addRow(v);
            }
            con.close();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "" + e.getMessage(), "Exception ", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

